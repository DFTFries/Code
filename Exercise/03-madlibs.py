# Madlibs game
# word game where you create a story
# by filling in blanks with random words

adjective1 = str(input("Enter an adjective (description): "))
adjective2 = str(input("Enter an adjective: "))
adjective3 = str(input("Enter an adjective: "))
noun1 = str(input("Enter a noun (person, place, thing): "))
verb1 = str(input("Enter a verb ending with -ing: "))

print(f"Today I went to a {adjective1} zoo.")
print(f"In an exhibit, I saw a {noun1}.")
print(f"{noun1} was {adjective2} and {verb1}.")
print(f"I was {adjective3}")